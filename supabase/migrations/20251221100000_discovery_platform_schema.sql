-- Function to generate hypothesis ID
CREATE OR REPLACE FUNCTION generate_hypothesis_id()
RETURNS TEXT AS $$
DECLARE
    year_part TEXT;
    seq_part TEXT;
    count INTEGER;
BEGIN
    year_part := TO_CHAR(NOW(), 'YYYY');
    SELECT COUNT(*) + 1 INTO count 
    FROM discovery_hypotheses 
    WHERE hypothesis_id LIKE 'HYP-' || year_part || '-%';
    seq_part := LPAD(count::TEXT, 3, '0');
    RETURN 'HYP-' || year_part || '-' || seq_part;
END;
$$ LANGUAGE plpgsql;

-- Function to clean expired cache
CREATE OR REPLACE FUNCTION cleanup_discovery_cache()
RETURNS INTEGER AS $$
DECLARE
    deleted_count INTEGER;
BEGIN
    DELETE FROM discovery_api_cache WHERE expires_at < NOW();
    GET DIAGNOSTICS deleted_count = ROW_COUNT;
    RETURN deleted_count;
END;
$$ LANGUAGE plpgsql;

-- Trigger to auto-generate hypothesis_id
CREATE OR REPLACE FUNCTION set_hypothesis_id()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.hypothesis_id IS NULL OR NEW.hypothesis_id = '' THEN
        NEW.hypothesis_id := generate_hypothesis_id();
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trigger_set_hypothesis_id
    BEFORE INSERT ON discovery_hypotheses
    FOR EACH ROW
    EXECUTE FUNCTION set_hypothesis_id();

-- Update timestamp trigger
CREATE OR REPLACE FUNCTION update_discovery_timestamp()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trigger_update_papers_timestamp
    BEFORE UPDATE ON discovery_papers
    FOR EACH ROW
    EXECUTE FUNCTION update_discovery_timestamp();

